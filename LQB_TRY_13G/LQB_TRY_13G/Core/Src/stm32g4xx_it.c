/**
  * @brief This function handles USART1 global interrupt / USART1 wake-up interrupt through EXTI line 25.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */

  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */
	if(__HAL_UART_GET_FLAG(&huart1,UART_FLAG_IDLE) != RESET)
	{
		__HAL_UART_CLEAR_IDLEFLAG(&huart1);
		HAL_UART_DMAStop(&huart1);
		unsigned char num=0;
		num = 100 - __HAL_DMA_GET_COUNTER(&hdma_usart1_rx);
		if(num == 1)
		{
			if(rxdata[0] == 'X')
			{
				sprintf(txdata,"X:%d\r\n",X);
				HAL_UART_Transmit(&huart1,(uint8_t *)txdata,strlen(txdata),50);
			}
			else if(rxdata[0] == 'Y')
			{
				sprintf(txdata,"Y:%d\r\n",Y);
				HAL_UART_Transmit(&huart1,(uint8_t *)txdata,strlen(txdata),50);
			}
			else if(rxdata[0] == '#')
			{
				lcd_sta = lcd_sta >=1?0:1;
				LCD_Clear(Black);
			}
			else HAL_UART_Transmit(&huart1,(uint8_t *)"Error1\r\n",strlen("Error1\r\n"),50);
		}
		else if(num == 3)
		{
			if(rxdata[0] == 'P' && rxdata[1] == 'A' && rxdata[2] == '1')
			{
				sprintf(txdata,"PA1:%.0f\r\n",PA1);
				HAL_UART_Transmit(&huart1,(uint8_t *)txdata,strlen(txdata),50);
			}
			else if(rxdata[0] == 'P' && rxdata[1] == 'A' && rxdata[2] == '4')
			{
				sprintf(txdata,"PA4:%.2f\r\n",PA4[1]);
				HAL_UART_Transmit(&huart1,(uint8_t *)txdata,strlen(txdata),50);
			}
			else if(rxdata[0] == 'P' && rxdata[1] == 'A' && rxdata[2] == '5')
			{
				sprintf(txdata,"PA5:%.2f\r\n",PA5[1]);
				HAL_UART_Transmit(&huart1,(uint8_t *)txdata,strlen(txdata),50);
			}
			else HAL_UART_Transmit(&huart1,(uint8_t *)"Error2\r\n",strlen("Error1\r\n"),50);
		}
		else HAL_UART_Transmit(&huart1,(uint8_t *)"Error3\r\n",strlen("Error3\r\n"),50);
		HAL_UART_Receive_DMA(&huart1,(uint8_t *)rxdata,100);
	}
  /* USER CODE END USART1_IRQn 1 */
}

