/**
  * @brief This function handles USART1 global interrupt / USART1 wake-up interrupt through EXTI line 25.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */

  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */
	if(__HAL_UART_GET_FLAG(&huart1,UART_FLAG_IDLE) != RESET)
	{
		__HAL_UART_CLEAR_IDLEFLAG(&huart1);
		HAL_UART_DMAStop(&huart1);
		unsigned int len = 100 - __HAL_DMA_GET_COUNTER(&hdma_usart1_rx);
		if(len == 1)
		{
			if(rxdata[0] == 'C') 
			{
				sprintf(tem,"T_U=%d\r\nH_U=%d\r\n%02d-%02d-%02d\r\n",(int)T_U,(int)H_U,GetTime.Hours,GetTime.Minutes,GetTime.Seconds);
				HAL_UART_Transmit_DMA(&huart1,(u8 *)tem,strlen(tem));
			}
			else if(rxdata[0] == 'T')
			{
				HAL_UART_Transmit_DMA(&huart1,(u8 *)data[0],strlen(data[0]));
			}
		}
		HAL_UART_Receive_DMA(&huart1,rxdata,100);
	}
  /* USER CODE END USART1_IRQn 1 */
}

